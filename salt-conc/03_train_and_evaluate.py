"""
=============================================================================
CrystalNet — Dual-Model Training Pipeline
=============================================================================

PURPOSE:
  Train two models that work together to predict crystallization conditions
  from a protein's amino acid sequence.

  Model A: Salt Type Classifier
    - Input:  ESM-2 embedding of protein sequence
    - Output: Which salt to use (NaCl, Ammonium Sulfate, KCl, etc.)

  Model B: Concentration Regressor
    - Input:  ESM-2 embedding + predicted salt type (one-hot encoded)
    - Output: Salt concentration in Molar (M)

  WHY TWO MODELS?
    Different salts have completely different concentration ranges in
    crystallization experiments:
      NaCl              → typically 0.05 – 0.5 M
      Ammonium Sulfate  → typically 1.0 – 3.0 M
      MgCl₂             → typically 0.01 – 0.2 M

    A single regressor predicting concentration without knowing the
    salt type would be confused by these overlapping ranges. Splitting
    into classification + regression gives each model a cleaner task.

INPUT FILE:
  crystallization_dataset.csv  (generated by 01_data_collection.py)
  Columns: pdb_id, sequence, seq_length, salt_concentration_M, salt_type, remark_280

OUTPUT:
  models/                       — saved model files for deployment
  plots_final/                  — presentation-ready evaluation plots

INSTALL:
  pip install torch transformers xgboost scikit-learn matplotlib seaborn pandas numpy joblib

RUN:
  python 04_dual_model_training.py

=============================================================================
"""

