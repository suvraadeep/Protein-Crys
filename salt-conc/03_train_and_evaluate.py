def main():
    ESM_MODEL = "facebook/esm2_t6_8M_UR50D"

    # ── 1. Clean data ──
    df = clean_dataset("crystallization_dataset.csv", min_salt_count=15)

    if len(df) < 50:
        print("\nFATAL: Too few samples after cleaning. "
              "Increase max_search in 01_data_collection.py and re-run.")
        return

    sequences = df["sequence"].tolist()
    y_conc = df["salt_concentration_M"].values
    y_log = df["log_salt"].values
    salt_labels = df["salt_display"].values

    # ── 2. Data overview plot ──
    plot_data_overview(df)

    # ── 3. Extract ESM-2 embeddings ──
    X_esm = extract_esm2_embeddings(sequences, model_name=ESM_MODEL, batch_size=8)

    # ── 4. Extract hand-crafted features (for comparison) ──
    X_hc, hc_names = extract_handcrafted(sequences)

    # ── 5. Train Model A: Salt Type Classifier ──
    le = LabelEncoder()
    y_enc = le.fit_transform(salt_labels)

    feature_sets = [
        ("Hand-Crafted", X_hc),
        ("ESM-2", X_esm),
        ("Combined", np.hstack([X_esm, X_hc]))
    ]

    classifier_results = {}
    for feature_name, X in feature_sets:
        clf, clf_scaler, clf_pca, metrics, all_results = train_salt_classifier(
            X, y_enc, le, feature_name=feature_name
        )
        classifier_results[feature_name] = {
            "clf": clf,
            "scaler": clf_scaler,
            "pca": clf_pca,
            "metrics": metrics,
            "all_results": all_results,
        }

    best_feature = max(
        classifier_results,
        key=lambda name: classifier_results[name]["metrics"]["cv_accuracy"]
    )
    best_clf_info = classifier_results[best_feature]
    print(f"\nBest overall Model A: {best_feature} features "
          f"(best inner model: {best_clf_info['metrics']['best_model']})")

    joblib.dump(best_clf_info["clf"], "models/salt_classifier.joblib")
    joblib.dump(best_clf_info["scaler"], "models/classifier_scaler.joblib")
    joblib.dump(best_clf_info["pca"], "models/classifier_pca.joblib")
    joblib.dump(le, "models/label_encoder.joblib")
    print("  Saved best salt classifier artifacts to models/")

    # ── 5b. Train DL classifier on combined features ──
    dl_results = train_dl_classifier(np.hstack([X_esm, X_hc]), y_enc, le,
                                     feature_name="DL Combined")

    # ── 6. Train Model B: Concentration Regressor ──
    reg, reg_scaler, reg_pca, reg_metrics, per_salt = \
        train_concentration_regressor(X_esm, y_conc, y_log, salt_labels, le)

    # ── 7. Feature comparison (the money slide) ──
    feat_results = compare_features(X_hc, X_esm, y_conc, y_log, salt_labels, le)

    # ── 8. t-SNE visualization ──
    plot_tsne(X_esm, y_conc, salt_labels)

    # ── 9. Save config ──
    save_config(ESM_MODEL, le)

    # ══════════════════════════════════════════════════════════════════════
    #  FINAL SUMMARY
    # ══════════════════════════════════════════════════════════════════════
    print("\n" + "=" * 60)
    print("TRAINING COMPLETE")
    print("=" * 60)

    print(f"\n  Dataset: {len(df)} samples, {df['salt_display'].nunique()} salt types")

    print(f"\n  MODEL A — Salt Type Classifier Results:")
    for feature_name, info in classifier_results.items():
        print(f"    {feature_name:10s} → Best model: {info['metrics']['best_model']:12s} | "
              f"Test Acc: {info['metrics']['accuracy']:.1%} | "
              f"CV Acc: {info['metrics']['cv_accuracy']:.1%}")

    print(f"\n  Best overall classifier: {best_feature} features "
          f"({best_clf_info['metrics']['best_model']})")

    print(f"\n  MODEL B — Concentration Regressor:")
    print(f"    MAE:  {reg_metrics['mae']:.4f} M")
    print(f"    RMSE: {reg_metrics['rmse']:.4f} M")
    print(f"    R²:   {reg_metrics['r2']:.4f}")

    print(f"\n  MODEL A — Deep Learning Classifier:")
    print(f"    Feature set: {dl_results['feature']}")
    print(f"    Test Accuracy: {dl_results['accuracy']:.1%}")
    print(f"    Best validation accuracy: {dl_results['best_val_acc']:.1%}")

    if feat_results:
        print(f"\n  FEATURE COMPARISON:")
        for name, r in feat_results.items():
            print(f"    {name:15s} → MAE: {r['mae']:.4f} M | R²: {r['r2']:.4f}")

    print(f"\n  SAVED FILES:")
    print(f"    models/          — all model artifacts (7 files + config.json)")
    print(f"    plots_final/     — all presentation plots (7 files)")

    print(f"\n  PLOTS GENERATED:")
    print(f"    00_data_overview.png           — dataset distribution")
    print(f"    01_salt_classifier_confusion.png — confusion matrix")
    print(f"    02_per_class_accuracy.png      — accuracy per salt type")
    print(f"    03_concentration_regressor.png  — pred vs actual + residuals")
    print(f"    04_per_salt_mae.png            — MAE breakdown by salt")
    print(f"    05_feature_comparison.png      — Hand-Crafted vs ESM-2")
    print(f"    06_tsne.png                    — embedding visualization")


